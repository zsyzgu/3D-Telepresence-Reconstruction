class Intrinsics {
public:
	float ppx, ppy;
	float fx, fy;

	CUDA_CALLABLE_MEMBER int2 translate(float3 pos) {
		return make_int2(floor(pos.x * fx / pos.z + ppx), floor(pos.y * fy / pos.z + ppy));
	}

	CUDA_CALLABLE_MEMBER float3 deproject(float2 pixel, float z) {
		return make_float3((pixel.x - ppx) / fx * z, (pixel.y - ppy) / fy * z, z);
	}

	CUDA_CALLABLE_MEMBER Intrinsics zoom(float x, float y) {
		Intrinsics result;
		result.ppx = ppx * x;
		result.ppy = ppy * y;
		result.fx = fx * x;
		result.fy = fy * y;
		return result;
	}

	CUDA_CALLABLE_MEMBER void output() {
		std::cout << "fx = " << fx << std::endl;
		std::cout << "fy = " << fy << std::endl;
		std::cout << "ppx = " << ppx << std::endl;
		std::cout << "ppy = " << ppy << std::endl;
	}
};

class Extrinsics {
public:
	float3 rotation0;
	float3 rotation1;
	float3 rotation2;
	float3 translation;
public:
	CUDA_CALLABLE_MEMBER Extrinsics& operator = (const Extrinsics& trans) {
		rotation0 = trans.rotation0;
		rotation1 = trans.rotation1;
		rotation2 = trans.rotation2;
		translation = trans.translation;
		return *this;
	}

	CUDA_CALLABLE_MEMBER Extrinsics() {
		setIdentity();
	}

	CUDA_CALLABLE_MEMBER Extrinsics(float* rotation, float* translation) { // Realsense Format
		rotation0 = make_float3(rotation[0], rotation[3], rotation[6]);
		rotation1 = make_float3(rotation[1], rotation[4], rotation[7]);
		rotation2 = make_float3(rotation[2], rotation[5], rotation[8]);
		this->translation = make_float3(translation[0], translation[1], translation[2]);
	}

	CUDA_CALLABLE_MEMBER Extrinsics(double* rotation, double* translation) { // Opencv Format
		rotation0 = make_float3(rotation[0], rotation[1], rotation[2]);
		rotation1 = make_float3(rotation[3], rotation[4], rotation[5]);
		rotation2 = make_float3(rotation[6], rotation[7], rotation[8]);
		this->translation = make_float3(translation[0], translation[1], translation[2]);
	}

	CUDA_CALLABLE_MEMBER float3 rotate(float3 pos) {
		return make_float3(dot(pos, rotation0), dot(pos, rotation1), dot(pos, rotation2));
	}

	CUDA_CALLABLE_MEMBER float3 translate(float3 pos) {
		return make_float3(dot(pos, rotation0), dot(pos, rotation1), dot(pos, rotation2)) + translation;
	}

	CUDA_CALLABLE_MEMBER float3 deltaZ() {
		return make_float3(rotation0.z, rotation1.z, rotation2.z);
	}

	CUDA_CALLABLE_MEMBER float3 col(int i) {
		if (i == 0) {
			return make_float3(rotation0.x, rotation1.x, rotation2.x);
		} else if (i == 1) {
			return make_float3(rotation0.y, rotation1.y, rotation2.y);
		} else if (i == 2) {
			return make_float3(rotation0.z, rotation1.z, rotation2.z);
		}
		return float3();
	}

	CUDA_CALLABLE_MEMBER void setIdentity() {
		rotation0 = make_float3(1, 0, 0);
		rotation1 = make_float3(0, 1, 0);
		rotation2 = make_float3(0, 0, 1);
		translation = make_float3(0, 0, 0);
	}

	CUDA_CALLABLE_MEMBER Extrinsics operator * (Extrinsics trans) {
		float3 col0 = trans.col(0);
		float3 col1 = trans.col(1);
		float3 col2 = trans.col(2);
		Extrinsics result;
		result.rotation0 = make_float3(dot(rotation0, col0), dot(rotation0, col1), dot(rotation0, col2));
		result.rotation1 = make_float3(dot(rotation1, col0), dot(rotation1, col1), dot(rotation1, col2));
		result.rotation2 = make_float3(dot(rotation2, col0), dot(rotation2, col1), dot(rotation2, col2));
		result.translation = make_float3(dot(rotation0, trans.translation), dot(rotation1, trans.translation), dot(rotation2, trans.translation)) + translation;
		return result;
	}

	CUDA_CALLABLE_MEMBER void output() {
		std::cout << rotation0.x << " " << rotation0.y << " " << rotation0.z << " " << 0 << std::endl;
		std::cout << rotation1.x << " " << rotation1.y << " " << rotation1.z << " " << 0 << std::endl;
		std::cout << rotation2.x << " " << rotation2.y << " " << rotation2.z << " " << 0 << std::endl;
		std::cout << translation.x << " " << translation.y << " " << translation.z << " " << 1 << std::endl;
	}
};

